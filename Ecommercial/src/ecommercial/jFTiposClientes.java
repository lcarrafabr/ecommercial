    public void getGradeTipoClientes() throws ParseException{
       //Consultar TipoClientes
        TiposClientes oTipoCliente = new TiposClientes();


        if ( tipojTextField.getText().length() > 0 ){
            oTipoCliente.setTipo(tipojTextField.getText()+"%");
        }

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"#Id", "Tipo", "Tabela de Preço", "Urgencia", "Bloquear Venda"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeTipoClientesjTable.setModel(modeloTabela);
        gradeTipoClientesjTable.getColumnModel().getColumn(0).setPreferredWidth(60);
        gradeTipoClientesjTable.getColumnModel().getColumn(1).setPreferredWidth(120);
        gradeTipoClientesjTable.getColumnModel().getColumn(2).setPreferredWidth(120);
        gradeTipoClientesjTable.getColumnModel().getColumn(3).setPreferredWidth(60);
        gradeTipoClientesjTable.getColumnModel().getColumn(3).setPreferredWidth(60);

        gradeTipoClientesjTable.setDefaultRenderer(Object.class, new ColorRenderer()); // DEIXA A GRADE ZEBRADA



try {


            resultSet = oTipoCliente.getConsultar();

            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(TiposClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("COD_TIPO_CLIENTE")),
                    resultSet.getString("TIPO"),
                    resultSet.getString("TABELA") ,
                    resultSet.getString("URGENCIA"),
                    resultSet.getString("BLOQUEAR_VENDA_INADIMPLENTE"),
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(TiposClientes.class.getName()).log(Level.SEVERE, null, ex);
        }

           }

    public void getFieldTipoCliente(int codTipoCliente) throws ParseException{

        TiposClientes oTipoCliente = new TiposClientes();
        ResultSet rs = null;
        oTipoCliente.setCod_tipo__cliente(codTipoCliente);

        try {
            rs = oTipoCliente.getConsultar();
            rs.next();

            ComboItem oItem = new ComboItem(rs.getInt("COD_TABELA_PRECO"), rs.getString("TABELA"));
            tipoTabelaPrecojComboBox.setSelectedItem(oItem);

            //Pegar campos
            tipoTabelaPrecojComboBox.setSelectedItem(rs.getString("TABELA"));
            tipojTextField.setText(rs.getString("TIPO"));
            urgenciajComboBox.setSelectedItem(rs.getString("URGENCIA"));
            bloquearVendaInadimplenmtejComboBox.setSelectedItem(rs.getString("BLOQUEAR_VENDA_INADIMPLENTE"));
            tempoMedioAtendimentojTextField.setText(rs.getString("TEMPO_MEDIO_ATENDIMENTO"));
            corjTextField.setText(rs.getString("COR"));

            String cor_rgb = (corjTextField.getText());

            String tar = cor_rgb;
            String [] teste = tar.split(",");


            Color color = new Color(Integer.parseInt(teste[0]),Integer.parseInt(teste[1]),Integer.parseInt(teste[2]));

            painelDeCorjPanel.setBackground(color);



//            painelDeCorjPanel.setBackground(cor_rgb);


        } catch (SQLException ex) {
            Logger.getLogger(TiposClientes.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

