    private void salvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButtonActionPerformed
        // TODO add your handling code here:
            salvarjButton.setEnabled(false);
            salvarMaisjButton.setEnabled(false);
            alterarjButton.setEnabled(false);
            excluirjButton.setEnabled(false);
            novojButton.setEnabled(true);
            editarjButton.setEnabled(true);

            combo_tipo_tabela_preco = (ComboItem) tipoTabelaPrecojComboBox.getSelectedItem();

            TiposClientes oTipoCliente = new TiposClientes();

            if (tipoTabelaPrecojComboBox.getSelectedItem().equals("")){

                JOptionPane.showMessageDialog(null, "Cadastre um tipo de tabela", "ATENÇÃO Mensagem do Sistema", JOptionPane.INFORMATION_MESSAGE);

            }else if (tipojTextField.getText().equals("")){

                JOptionPane.showMessageDialog(null, "O campo ESTADO é obrigatório", "ATENÇÃO Mensagem do Sistema", JOptionPane.INFORMATION_MESSAGE);

            }else{

                oTipoCliente.setCod_tabela_preco(combo_tipo_tabela_preco.getId());
                oTipoCliente.setTipo(tipojTextField.getText().trim().toUpperCase());
                oTipoCliente.setUrgencia(urgenciajComboBox.getSelectedItem().toString());
                oTipoCliente.setBloquear_venda_inadimplente(bloquearVendaInadimplenmtejComboBox.getSelectedItem().toString());
                oTipoCliente.setTempo_medio_atendimento(Float.parseFloat(tempoMedioAtendimentojTextField.getText()));
                oTipoCliente.setCor(corjTextField.getText());
                oTipoCliente.setDescricao(descricaojTextArea.getText());
                oTipoCliente.setCadastrar();
            try {
                ClassConecta.con.commit();
                ClassConecta.con.setAutoCommit(true);
            } catch (SQLException ex) {
                Logger.getLogger(jFTiposClientes.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                getGradeTipoClientes();
            } catch (ParseException ex) {
                Logger.getLogger(jFTiposClientes.class.getName()).log(Level.SEVERE, null, ex);
            }


            }
    }//GEN-LAST:event_salvarjButtonActionPerformed

