    private void paisesjComboBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_paisesjComboBoxItemStateChanged
        // TODO add your handling code here:
        try {
            Estados oEstado = new Estados();
            ResultSet rs = null;

            combopais = (ComboItem) paisesjComboBox.getSelectedItem();
            oEstado.setCod_pais(combopais.getId());
            rs = oEstado.getConsultarEstados();          
            estadosjComboBox.removeAllItems();
            while (rs.next()) {
                estadosjComboBox.addItem(new ComboItem(rs.getInt("COD_ESTADO"), rs.getString("ESTADO")));
            }
            estadosjComboBox.updateUI();
        } catch (SQLException ex) {
            Logger.getLogger(jFEstados.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_paisesjComboBoxItemStateChanged

