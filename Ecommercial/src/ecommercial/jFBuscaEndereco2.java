    public void getGradeLogradouros() throws ParseException, SQLException{
       //Consultar Logradouros
        Logradouros oLogradouro = new Logradouros();


        if ( logradourojTextField.getText().length() > 0 ){
            oLogradouro.setLogradouro(logradourojTextField.getText()+"%");
        }

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"#Id", "Tipo", "Logradouro", "Bairro", "Cidade", "Estado", "Pa√≠s"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeLogradourosjTable.setModel(modeloTabela);
        gradeLogradourosjTable.getColumnModel().getColumn(0).setPreferredWidth(25);
        gradeLogradourosjTable.getColumnModel().getColumn(1).setPreferredWidth(30);
        gradeLogradourosjTable.getColumnModel().getColumn(2).setPreferredWidth(120);
        gradeLogradourosjTable.getColumnModel().getColumn(3).setPreferredWidth(80);
        gradeLogradourosjTable.getColumnModel().getColumn(4).setPreferredWidth(80);
        gradeLogradourosjTable.getColumnModel().getColumn(5).setPreferredWidth(80);
        gradeLogradourosjTable.getColumnModel().getColumn(6).setPreferredWidth(80);



try {


            resultSet = oLogradouro.getConsultar();

            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(Logradouros.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("COD_LOGRADOURO")),
                    resultSet.getString("TIPO"),
                    resultSet.getString("LOGRADOURO") ,
                    resultSet.getString("BAIRRO"),
                    resultSet.getString("CIDADE"),
                    resultSet.getString("ESTADO"),
                    resultSet.getString("PAIS"),
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(Logradouros.class.getName()).log(Level.SEVERE, null, ex);
        }

           }


    public void getFieldLogradouro(int codLogradouro) throws ParseException{

        Logradouros oLogradouro = new Logradouros();
        ResultSet rs = null;
        oLogradouro.setCod_logradouro(codLogradouro);

        try {
            rs = oLogradouro.getConsultar();
            rs.next();

            ComboItem oItem = new ComboItem(rs.getInt("COD_TIPO_LOGRADOURO"), rs.getString("TIPO"));

            //Pegar campos
            mensagemjLabel.setText(rs.getString("TIPO") + " " + rs.getString("LOGRADOURO") + ", " + rs.getString("BAIRRO") + ", " + rs.getString("ESTADO")+
                          " - " + rs.getString("SIGLA") + " - " + rs.getString("PAIS"));


        } catch (SQLException ex) {
            Logger.getLogger(Logradouros.class.getName()).log(Level.SEVERE, null, ex);
        }
    }




