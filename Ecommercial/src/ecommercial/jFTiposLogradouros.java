    public void getGradeTiposLogradouros() throws ParseException{
       //Consultar Tipos Logradouros
        TiposLogradouros oTipoLogradouro = new TiposLogradouros();

        if ( tipoLogradourojTextField.getText().length() > 0 ){
            oTipoLogradouro.setTipo(tipoLogradourojTextField.getText()+"%");
        }

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"#Id", "Tipo Logradouro", "Num. Tipo"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeTiposLogradourosjTable.setModel(modeloTabela);
        gradeTiposLogradourosjTable.getColumnModel().getColumn(0).setPreferredWidth(50);
        gradeTiposLogradourosjTable.getColumnModel().getColumn(1).setPreferredWidth(100);
        gradeTiposLogradourosjTable.getColumnModel().getColumn(2).setPreferredWidth(80);




try {


            resultSet = oTipoLogradouro.getConsultar();

            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(TiposLogradouros.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("COD_TIPO_LOGRADOURO")),
                    resultSet.getString("TIPO"),
                    resultSet.getString("NUM_TIPO") ,
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(TiposLogradouros.class.getName()).log(Level.SEVERE, null, ex);
        }

           }

    public void getFieldTipoLogradouro(int codTipoLogradouro) throws ParseException{

        TiposLogradouros oTipoLogradouro = new TiposLogradouros();
        ResultSet rs = null;
        oTipoLogradouro.setCod_tipo_logradouro(codTipoLogradouro);

        try {
            rs = oTipoLogradouro.getConsultar();
            rs.next();

            //Pegar campos
            tipoLogradourojTextField.setText(rs.getString("TIPO"));
            numTipojTextField.setText(rs.getString("NUM_TIPO"));

        } catch (SQLException ex) {
            Logger.getLogger(TiposLogradouros.class.getName()).log(Level.SEVERE, null, ex);
        }



    }

