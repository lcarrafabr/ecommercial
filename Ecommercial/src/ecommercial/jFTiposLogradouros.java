    private void excluirjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirjButtonActionPerformed
        // TODO add your handling code here:
        Object[] options = {"Sim", "Não"};
        int sd =
                JOptionPane.showOptionDialog(null, "Deseja realmente EXCLUIR esse registro?", "Confirmação!", JOptionPane.DEFAULT_OPTION,
                JOptionPane.WARNING_MESSAGE, null, options, options[0]);

        if (sd != 0) {
            return;
        }
        alterarjButton.setEnabled(false);
        editarjButton.setEnabled(true);
        excluirjButton.setEnabled(false);
        novojButton.setEnabled(true);

        TiposLogradouros oTipoLogradouro = new TiposLogradouros();
        oTipoLogradouro.setCod_tipo_logradouro(codTipoLogradouro);

        oTipoLogradouro.setExcluir(conexao);
        try {
            ClassConecta.con.commit();
            ClassConecta.con.setAutoCommit(true);
        } catch (SQLException ex) {
            Logger.getLogger(jFTiposLogradouros.class.getName()).log(Level.SEVERE, null, ex);
        }

        tipoLogradourojTextField.setText("");
        numTipojTextField.setText("");
        try {
            getGradeTiposLogradouros();
        } catch (ParseException ex) {
            Logger.getLogger(jFTiposLogradouros.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_excluirjButtonActionPerformed

    private void tipoLogradourojTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tipoLogradourojTextFieldKeyReleased
        // TODO add your handling code here:
        if (!alterarjButton.isEnabled()){
            try {
                TiposLogradouros oTipoLogradouro = new TiposLogradouros();
                oTipoLogradouro.setTipo(tipoLogradourojTextField.getText());
                ResultSet rs = null;
                rs = oTipoLogradouro.getConsultar();
                getGradeTiposLogradouros();
            } catch (ParseException ex) {
                Logger.getLogger(jFTiposLogradouros.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(jFTiposLogradouros.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (tipoLogradourojTextField.getText().equals("")){
            tipoLogradourojTextField.setText("");
            numTipojTextField.setText("");
            editarjButton.setEnabled(false);
        }
    }//GEN-LAST:event_tipoLogradourojTextFieldKeyReleased

