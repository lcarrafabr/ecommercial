    public void getGradeAtividades() throws ParseException{
       //Consultar Atividades
        Atividades oAtividade = new Atividades();


        if ( atividadejTextField.getText().length() > 0 ){
            oAtividade.setAtividade(atividadejTextField.getText()+"%");
        }

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"#Id", "Atividades", "Ramo Atividade"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeAtividadesjTable.setModel(modeloTabela);
        gradeAtividadesjTable.getColumnModel().getColumn(0).setPreferredWidth(10);
        gradeAtividadesjTable.getColumnModel().getColumn(1).setPreferredWidth(150);
        gradeAtividadesjTable.getColumnModel().getColumn(2).setPreferredWidth(150);

try {


            resultSet = oAtividade.getConsultar();

            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(Atividades.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("COD_ATIVIDADE")),
                    resultSet.getString("ATIVIDADE"),
                    resultSet.getString("RAMO_ATIVIDADE") ,
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(Atividades.class.getName()).log(Level.SEVERE, null, ex);
        }

           }

    public void getFieldAtividades(int codAtividade) throws ParseException{

        Atividades oAtividade = new Atividades();
        ResultSet rs = null;
        oAtividade.setCod_atividade(codAtividade);

        try {
            rs = oAtividade.getConsultar();
            rs.next();

            ComboItem oItem = new ComboItem(rs.getInt("COD_RAMO_ATIVIDADE"), rs.getString("RAMO_ATIVIDADE"));
            ramoAtividadejComboBox.setSelectedItem(oItem);

            //Pegar campos
            atividadejTextField.setText(rs.getString("ATIVIDADE"));
        } catch (SQLException ex) {
            Logger.getLogger(Atividades.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


