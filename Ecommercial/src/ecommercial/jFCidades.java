     public void getFieldCidade(int codCidade) throws ParseException{

        Cidades oCidade = new Cidades();
        ResultSet rs = null;
        oCidade.setCod_cidade(codCidade);

        try {
            rs = oCidade.getConsultar();
            rs.next();

            ComboItem oItem = new ComboItem(rs.getInt("COD_ESTADO"), rs.getString("ESTADO"));
            estadojComboBox.setSelectedItem(oItem);

            //Pegar campos
            cidadejTextField.setText(rs.getString("CIDADE"));
            codIbgejTextField.setText(rs.getString("COD_IBGE"));
            estadojComboBox.setSelectedItem(rs.getString("ESTADO"));

        } catch (SQLException ex) {
            Logger.getLogger(Estados.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


