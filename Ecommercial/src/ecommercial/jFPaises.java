    private void salvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButtonActionPerformed
        try {
            // TODO add your handling code here:
            salvarjButton.setEnabled(false);
            salvarMaisjButton.setEnabled(false);
            alterarjButton.setEnabled(false);
            excluirjButton.setEnabled(false);
            novojButton.setEnabled(true);
            editarjButton.setEnabled(true);

            Paises oPais = new Paises();

            //VERIFICA SE OS CAMPOS "PAIS" E "COD INTERNACIONAL" ESTÃO VAZIOS. SE ESTIVER VAZIO ENVIE MENSAGEM E CLIQUE NO BOTÃO NOVO
            if (paisjTextField.getText().equals("")){

                JOptionPane.showMessageDialog(null, "O campo PAÍS é obrigatório", "ATENÇÃO Mensagem do Sistema", JOptionPane.INFORMATION_MESSAGE);
                novojButtonActionPerformed(null);

            }else if (codInternacionaljTextField.getText().equals("")){
                JOptionPane.showMessageDialog(null, "O campo COD. INTERNACIONAL é obrigatório", "ATENÇÃO Mensagem do Sistema", JOptionPane.INFORMATION_MESSAGE);
                novojButtonActionPerformed(null);
            }else{

            oPais.setPais(paisjTextField.getText().trim().toUpperCase());
            oPais.setCod_internacional(Integer.parseInt(codInternacionaljTextField.getText()));
            oPais.setRegiao(Integer.parseInt(regiaojTextField.getText()));
            oPais.setCadastrar();

            ClassConecta.con.commit();
            ClassConecta.con.setAutoCommit(true);
            }//Fim do da verificação nula de campos

        } catch (SQLException ex) {
            Logger.getLogger(jFPaises.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            getGradePaises();
        } catch (ParseException ex) {
            Logger.getLogger(jFPaises.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_salvarjButtonActionPerformed

