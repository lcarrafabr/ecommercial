    public void getGradePaises() throws ParseException{
       //Consultar pedidos
        Paises oPais = new Paises();

        if ( paisjTextField.getText().length() > 0 ){
            oPais.setPais(paisjTextField.getText()+"%");
        }

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"#Id", "País", "Cód. Internacional", "Região"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradePaisjTable.setModel(modeloTabela);
        gradePaisjTable.getColumnModel().getColumn(0).setPreferredWidth(10);
        gradePaisjTable.getColumnModel().getColumn(1).setPreferredWidth(150);
        gradePaisjTable.getColumnModel().getColumn(2).setPreferredWidth(50);
        gradePaisjTable.getColumnModel().getColumn(3).setPreferredWidth(50);



try {


            resultSet = oPais.getConsultar();

            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(Paises.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("COD_PAIS")),
                    resultSet.getString("PAIS"),
                    resultSet.getString("COD_INTERNACIONAL") ,
                    resultSet.getString("REGIAO"),
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(Paises.class.getName()).log(Level.SEVERE, null, ex);
        }

           }


     public void getFieldPais(int codTelefone) throws ParseException{

        Paises oPais = new Paises();
        ResultSet rs = null;
        oPais.setCod_pais(codPais);

        try {
            rs = oPais.getConsultar();
            rs.next();

            //Pegar campos
            paisjTextField.setText(rs.getString("PAIS"));
            codInternacionaljTextField.setText(String.valueOf(rs.getInt("COD_INTERNACIONAL")));
            regiaojTextField.setText(String.valueOf(rs.getInt("REGIAO")));

        } catch (SQLException ex) {
            Logger.getLogger(Paises.class.getName()).log(Level.SEVERE, null, ex);
        }



    }

